public class loop {
    public static void main(String args[]){

    int i;
    System.out.println("for loop");
    for(i=0;i<=5;i++){
        System.out.println("value of i is  :"+i);
    }
    System.out.println("while loop");
    while(i<=10){
        System.out.println("value of i is  :"+i);
        i++;
    }

    System.out.println("doo while loop");

    do{
        System.out.println("value of i is  :"+i);
        i++;
    }
    while(i<=15);
    }
}
